/* Interrupt Service Routines */
IFX_INTERRUPT(asclin0TxISR, 0, INTPRIO_ASCLIN0_TX);
void asclin0TxISR(void)
{
    IfxAsclin_Asc_isrTransmit(&g_btAsc);
}

IFX_INTERRUPT(asclin0RxISR, 0, INTPRIO_ASCLIN0_RX);
void asclin0RxISR(void)
{
    IfxAsclin_Asc_isrReceive(&g_btAsc);
}

/***********/

/* BT Task */

volatile uint32 g_btRxByteCount = 0;
volatile uint8  g_btLastRxByte = 0;
volatile uint32 g_btRxOverflowCount = 0;

void BT_Task(void)
{
    uint8 ch;
    uint16 count = 0;

    while (BT_ReadByte(&ch))
    {
        g_btLastRxByte = ch;
        g_btRxByteCount++;

        uint16 next = (g_btRxWrite + 1) % sizeof(g_btRxRing);
        if (next != g_btRxRead)
        {
            g_btRxRing[g_btRxWrite] = ch;
            g_btRxWrite = next;
        }
        else
        {
            g_btRxOverflowCount++;
            break;
        }

        count++;
        if (count >= 64)
        {
            break;
        }
    }
}

