#define BT_BAUDRATE             9600
#define BT_TX_BUFFER_SIZE       128
#define BT_RX_BUFFER_SIZE       128

// 예시 핀: 실제 보드 핀맵에 맞게 수정 필요
#define BT_PIN_RX               IfxAsclin0_RXB_P15_3_IN
#define BT_PIN_TX               IfxAsclin0_TX_P15_2_OUT

/* Definition of the interrupt priorities */
#define INTPRIO_ASCLIN0_RX      22
#define INTPRIO_ASCLIN0_TX      23

#define UART_RX_BUFFER_SIZE     64                                      /* Definition of the receive buffer size    */
#define UART_TX_BUFFER_SIZE     64                                      /* Definition of the transmit buffer size   */
#define SIZE                    13                                      /* Size of the string                       */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
/* Declaration of the ASC handle */
static IfxAsclin_Asc g_btAsc;

/* Declaration of the FIFOs parameters */
static uint8 g_btTxBuffer[BT_TX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];
static uint8 g_btRxBuffer[BT_RX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];

static uint8 g_btRxRing[128];
static volatile uint16 g_btRxWrite = 0;
static volatile uint16 g_btRxRead  = 0;

/*********************************************************************************************************************/
/*-----------------------------------------------Function Prototypes-------------------------------------------------*/
/*********************************************************************************************************************/

boolean BT_ReadByte(uint8 *ch);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
