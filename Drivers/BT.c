/* BT Library */

void BT_SendBytes(const uint8 *data, Ifx_SizeT len)
{
    Ifx_SizeT count = len;
    IfxAsclin_Asc_write(&g_btAsc, (void *)data, &count, TIME_INFINITE);
}

void BT_SendString(const char *str)
{
    Ifx_SizeT count = strlen(str);
    IfxAsclin_Asc_write(&g_btAsc, (void *)str, &count, TIME_INFINITE);
}

boolean BT_ReadByte(uint8 *ch)
{
    Ifx_SizeT count = 1;
    IfxAsclin_Asc_read(&g_btAsc, ch, &count, 1);
    return (count == 1) ? TRUE : FALSE;
}

boolean BT_GetChar(uint8 *ch)
{
    if (g_btRxRead == g_btRxWrite)
    {
        return FALSE;
    }

    *ch = g_btRxRing[g_btRxRead];
    g_btRxRead = (uint16)((g_btRxRead + 1U) % sizeof(g_btRxRing));
    return TRUE;
}

UserId_t BT_GetSelectedUser(void)
{
    return g_btSelectedUser;
}

boolean BT_IsNearby(void)
{
    return g_btNearby;
}
