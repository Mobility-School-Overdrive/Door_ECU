/* BT Init */

/* This function initializes the ASCLIN UART module */
void init_ASCLIN0_UART(void)
{
    /* Initialize an instance of IfxAsclin_Asc_Config with default values */
    IfxAsclin_Asc_Config ascConfig;
    IfxAsclin_Asc_initModuleConfig(&ascConfig, &MODULE_ASCLIN0);

    /* Set the desired baud rate */
    ascConfig.baudrate.baudrate = BT_BAUDRATE;

    /* ISR priorities and interrupt target */
    ascConfig.interrupt.txPriority = INTPRIO_ASCLIN0_TX;
    ascConfig.interrupt.rxPriority = INTPRIO_ASCLIN0_RX;
    ascConfig.interrupt.typeOfService = IfxCpu_Irq_getTos(IfxCpu_getCoreIndex());

    /* FIFO configuration */
    ascConfig.txBuffer = &g_btTxBuffer;
    ascConfig.txBufferSize = BT_TX_BUFFER_SIZE;
    ascConfig.rxBuffer = &g_btRxBuffer;
    ascConfig.rxBufferSize = BT_RX_BUFFER_SIZE;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins =
    {
        NULL_PTR,       IfxPort_InputMode_pullUp,     /* CTS pin not used */
        &BT_PIN_RX,   IfxPort_InputMode_pullUp,       /* RX pin           */
        NULL_PTR,       IfxPort_OutputMode_pushPull,  /* RTS pin not used */
        &BT_PIN_TX,   IfxPort_OutputMode_pushPull,    /* TX pin           */
        IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConfig.pins = &pins;

    IfxAsclin_Asc_initModule(&g_btAsc, &ascConfig); /* Initialize module with above parameters */
}

void initBT(void) {
    init_ASCLIN0_UART();
}

