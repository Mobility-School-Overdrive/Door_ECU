/* BT Task */

void BT_Task(void)
{
    uint8 ch;
    uint16 count = 0;

    while (BT_ReadByte(&ch))
    {
        g_btLastRxByte = ch;
        g_btRxByteCount++;

        {
            uint16 next = (uint16)((g_btRxWrite + 1U) % sizeof(g_btRxRing));
            if (next != g_btRxRead)
            {
                g_btRxRing[g_btRxWrite] = ch;
                g_btRxWrite = next;
            }
            else
            {
                g_btRxOverflowCount++;
                break;
            }
        }

        count++;
        if (count >= 64U)
        {
            break;
        }
    }
}

void BT_LineTask(void)
{
    static uint8 idx = 0U;
    uint8 ch;

    while (BT_GetChar(&ch))
    {
        if ((ch != '\r') && (ch != '\n'))
        {
            if (idx < (sizeof(g_btWorkLine) - 1U))
            {
                g_btWorkLine[idx++] = ch;
            }
        }

        if ((ch == '\r') || (ch == '\n'))
        {
            if (idx > 0U)
            {
                g_btWorkLine[idx] = '\0';
                strcpy((char *)g_btLine, (char *)g_btWorkLine);
                g_btLineReady = TRUE;
                idx = 0U;
            }
        }
    }
}

