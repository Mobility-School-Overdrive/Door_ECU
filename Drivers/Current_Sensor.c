/* Task */
void PinchMonitor_Task(void)
{
    CurrentFaultType doorFault;
    CurrentFaultType windowFault;
    float32 currentAngle;
    float32 recoveryTarget;

    doorFault = CurrentSensor_GetFault(DOOR_MOTOR_ID);
    windowFault = CurrentSensor_GetFault(WINDOW_MOTOR_ID);

    /* -------------------------------------------------
     * Door pinch
     * ------------------------------------------------- */
    if (doorFault == CURRENT_FAULT_MOTOR)
    {
        /* 즉시 정지 */
        Door_Motor_Stop();

        /* 전류 감시 중지 */
        CurrentSensor_StopSense(DOOR_MOTOR_ID);

        /* CAN 알림 */
        can_send_PinchDetection(PINCH_TYPE_DOOR);

        /* 버저 경고 */
        Buzzer_Request(BUZ_EVENT_PINCH);

        /* 현재 각도 기준 반대 방향 10도 복귀 */
        currentAngle = Door_Motor_GetCurrentAngle();

        if (g_rxDoorOpenCmd == OPEN_CLOSE_CLOSE)
        {
            /* 닫히는 중 끼임 -> 다시 열기 */
            recoveryTarget = currentAngle + DOOR_PINCH_RECOVERY_ANGLE_DEG;
        }
        else
        {
            /* 열리는 중 끼임 -> 다시 닫기 */
            recoveryTarget = currentAngle - DOOR_PINCH_RECOVERY_ANGLE_DEG;
        }

        recoveryTarget = clampFloat32(recoveryTarget,
                                      DOOR_MIN_ANGLE_DEG,
                                      DOOR_MAX_ANGLE_DEG);

        CurrentSensor_ClearFault(DOOR_MOTOR_ID);
        Door_Motor_SetTarget(recoveryTarget);
    }

    /* -------------------------------------------------
     * Window pinch
     * ------------------------------------------------- */
    if (windowFault == CURRENT_FAULT_MOTOR)
    {
        Window_Motor_Stop();
        CurrentSensor_StopSense(WINDOW_MOTOR_ID);

        can_send_PinchDetection(PINCH_TYPE_WINDOW);

        /* 버저 경고 */
        Buzzer_Request(BUZ_EVENT_PINCH);

        currentAngle = Window_Motor_GetCurrentAngle();

        if (g_rxWindowCmd == OPEN_CLOSE_CLOSE)
        {
            recoveryTarget = currentAngle + WINDOW_PINCH_RECOVERY_ANGLE_DEG;
        }
        else
        {
            recoveryTarget = currentAngle - WINDOW_PINCH_RECOVERY_ANGLE_DEG;
        }

        recoveryTarget = clampFloat32(recoveryTarget,
                                      WINDOW_MIN_ANGLE_DEG,
                                      WINDOW_MAX_ANGLE_DEG);

        CurrentSensor_ClearFault(WINDOW_MOTOR_ID);
        Window_Motor_SetTarget(recoveryTarget);
    }
}

void CurrentSensor_Task(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);
    IfxEvadc_Adc_Channel *channel = NULL;

    float32 FILTER_ALPHA;
    uint8 START_BLANK_COUNT;
    uint8 BASELINE_SAMPLES;
    float32 JAM_RAW_DIFF_TH;
    uint8 JAM_COUNT_TH;

    switch (motorId) {
        case DOOR_MOTOR_ID:
            channel = &g_curDoorChannel;

            FILTER_ALPHA = DOOR_FILTER_ALPHA;
            START_BLANK_COUNT = DOOR_START_BLANK_COUNT;
            BASELINE_SAMPLES = DOOR_BASELINE_SAMPLES;
            JAM_RAW_DIFF_TH = DOOR_JAM_RAW_DIFF_TH ;
            JAM_COUNT_TH = DOOR_JAM_COUNT_TH;
            break;

       case WINDOW_MOTOR_ID:
            channel = &g_curWindowChannel;

            FILTER_ALPHA = WINDOW_FILTER_ALPHA;
            START_BLANK_COUNT = WINDOW_START_BLANK_COUNT;
            BASELINE_SAMPLES = WINDOW_BASELINE_SAMPLES;
            JAM_RAW_DIFF_TH = WINDOW_JAM_RAW_DIFF_TH ;
            JAM_COUNT_TH = WINDOW_JAM_COUNT_TH;
            break;
    }

    Ifx_EVADC_G_RES result;
    uint16 raw;
    float32 rawF;

    if (sensor->senseActive == FALSE)
    {
        return;
    }

    if (sensor->faultLatched == TRUE)
    {
        return;
    }

    result = IfxEvadc_Adc_getResult(channel);

    if (result.B.VF != 1)
    {
        return;
    }

    raw = (uint16)result.B.RESULT;
    sensor->curRaw = raw;
    rawF = (float32)raw;

    sensor->curVolt = (rawF * CUR_REF_VOLTAGE) / CUR_ADC_MAX;
    sensor->curAmp  = Current_CalcAmp(sensor->curVolt, sensor->zeroVolt);

    if (sensor->curValid == FALSE)
    {
        sensor->filtRaw     = rawF;
        sensor->baseRaw     = rawF;
        sensor->diffRaw     = 0.0f;
        sensor->peakDiffRaw = 0.0f;
        sensor->jamCnt      = 0;
        sensor->spike       = FALSE;
        sensor->curValid    = TRUE;
        return;
    }

    sensor->filtRaw = (FILTER_ALPHA * rawF) +
                      ((1.0f - FILTER_ALPHA) * sensor->filtRaw);

    if (sensor->blankCnt < START_BLANK_COUNT)
    {
        sensor->blankCnt++;
        sensor->diffRaw = 0.0f;
        sensor->jamCnt = 0;
        return;
    }

    if (sensor->monitoring == FALSE)
    {
        sensor->baseRaw = ((sensor->baseRaw * (float32)sensor->baseCnt) + sensor->filtRaw) /
                          ((float32)sensor->baseCnt + 1.0f);

        if (sensor->baseCnt < BASELINE_SAMPLES)
        {
            sensor->baseCnt++;
            sensor->diffRaw = 0.0f;
            sensor->jamCnt = 0;
            return;
        }
        else
        {
            sensor->monitoring = TRUE;
            sensor->diffRaw = 0.0f;
            sensor->jamCnt = 0;
            return;
        }
    }

    sensor->diffRaw = fabsf(sensor->filtRaw - sensor->baseRaw);

    if (sensor->diffRaw > sensor->peakDiffRaw)
    {
        sensor->peakDiffRaw = sensor->diffRaw;
    }

    if (sensor->fault == CURRENT_FAULT_NONE)
    {
        if (sensor->diffRaw >= JAM_RAW_DIFF_TH)
        {
            if (sensor->jamCnt < 255)
            {
                sensor->jamCnt++;
            }
        }
        else
        {
            sensor->jamCnt = 0;
        }

        if (sensor->jamCnt >= JAM_COUNT_TH)
        {
            sensor->spike = TRUE;
            sensor->faultLatched = TRUE;
            sensor->fault = CURRENT_FAULT_MOTOR;
        }
    }
}

