/* AN1 = Group0 Channel1, AN2 = Group0 Channel2 */
#define DOOR_CHANNEL_ID         IfxEvadc_ChannelId_1
#define DOOR_RESULT_REG         IfxEvadc_ChannelResult_1

#define WINDOW_CHANNEL_ID       IfxEvadc_ChannelId_2
#define WINDOW_RESULT_REG       IfxEvadc_ChannelResult_2

#define CUR_REF_VOLTAGE         5.0f
#define CUR_ADC_MAX             4095.0f

/* ACS712 설정 */
#define ACS_ZERO_VOLT_DEFAULT   2.5f
#define ACS_SENSITIVITY         0.1f        /* 20A 버전 : 100mV/A */

/* MOTER ID */
#define DOOR_MOTOR_ID   1
#define WINDOW_MOTOR_ID 2

/* DOOR 감시 파라미터 (RAW 기준) */
#define DOOR_FILTER_ALPHA           0.2f
#define DOOR_START_BLANK_COUNT      8         /* 10ms task 기준 약 80ms */
#define DOOR_BASELINE_SAMPLES       20
#define DOOR_JAM_RAW_DIFF_TH        18000.0f    // 끼임 후보
#define DOOR_JAM_COUNT_TH           3

/* WINDOW 감시 파라미터 (RAW 기준) */
#define WINDOW_FILTER_ALPHA         0.2f
#define WINDOW_START_BLANK_COUNT    8         /* 10ms task 기준 약 80ms */
#define WINDOW_BASELINE_SAMPLES     20
#define WINDOW_JAM_RAW_DIFF_TH      220000.0f    // 끼임 후보
#define WINDOW_JAM_COUNT_TH         3

/* 인터럽트 */
#define DOOR_ISR_PRIORITY       5
#define WINDOW_ISR_PRIORITY     6
#define CUR_ISR_PROVIDER        IfxSrc_Tos_cpu0

