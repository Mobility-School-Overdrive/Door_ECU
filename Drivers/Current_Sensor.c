/* Current Sensor Library */
static CurrentSensorState* getCurrentSensor(uint8 motorId)
{
    switch (motorId) {
        case DOOR_MOTOR_ID:
            return &g_doorSensor;

        case WINDOW_MOTOR_ID:
            return &g_windowSensor;

        default:
            return NULL;
    }
}

static float32 Current_CalcAmp(float32 voltage, float32 zeroVolt)
{
    return (voltage - zeroVolt) / ACS_SENSITIVITY;
}

float32 CurrentSensor_GetAmp(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    return sensor->curAmp;
}

uint16 CurrentSensor_GetRaw(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    return sensor->curRaw;
}

CurrentFaultType CurrentSensor_GetFault(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    return sensor->fault;
}

void CurrentSensor_ClearFault(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    sensor->spike = FALSE;
    sensor->fault = CURRENT_FAULT_NONE;
    sensor->faultLatched = FALSE;
}

void CurrentSensor_StartSense(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    sensor->senseActive   = TRUE;
    sensor->monitoring    = FALSE;
    sensor->faultLatched  = FALSE;

    sensor->blankCnt      = 0;
    sensor->baseCnt       = 0;
    sensor->jamCnt        = 0;

    sensor->spike         = FALSE;
    sensor->fault         = CURRENT_FAULT_NONE;

    sensor->curValid      = FALSE;

    sensor->filtRaw       = 0.0f;
    sensor->baseRaw       = 0.0f;
    sensor->diffRaw       = 0.0f;
    sensor->peakDiffRaw   = 0.0f;
}

void CurrentSensor_StopSense(uint8 motorId)
{
    CurrentSensorState *sensor = getCurrentSensor(motorId);

    sensor->senseActive   = FALSE;
    sensor->monitoring    = FALSE;
    sensor->faultLatched  = FALSE;

    sensor->blankCnt      = 0;
    sensor->baseCnt       = 0;
    sensor->jamCnt        = 0;

    sensor->filtRaw       = 0.0f;
    sensor->baseRaw       = 0.0f;
    sensor->diffRaw       = 0.0f;
}
