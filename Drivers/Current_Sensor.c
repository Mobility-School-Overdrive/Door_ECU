/* EVADC handle */
static IfxEvadc_Adc_Channel g_curDoorChannel;
static IfxEvadc_Adc_Channel g_curWindowChannel;

/* Current Sensor Value */
typedef struct
{
    /* ADC result / converted value */
    volatile uint16  curRaw;
    volatile float32 curVolt;
    volatile float32 curAmp;

    /* state */
    volatile boolean spike;
    volatile boolean curValid;

    /* jam detection */
    volatile uint8   jamCnt;
    volatile float32 filtRaw;
    volatile float32 baseRaw;
    volatile float32 diffRaw;
    volatile float32 peakDiffRaw;

    /* sensing flow */
    volatile boolean senseActive;
    volatile boolean monitoring;
    volatile boolean faultLatched;

    volatile uint16  blankCnt;
    volatile uint16  baseCnt;

    /* sensor calibration */
    volatile float32 zeroVolt;

    /* fault */
    volatile CurrentFaultType fault;
} CurrentSensorState;

static CurrentSensorState g_doorSensor =
{
    .zeroVolt = ACS_ZERO_VOLT_DEFAULT,
    .fault    = CURRENT_FAULT_NONE
};

static CurrentSensorState g_windowSensor =
{
    .zeroVolt = ACS_ZERO_VOLT_DEFAULT,
    .fault    = CURRENT_FAULT_NONE
};

