/* Current Sensor Init */

void initCurrentSensor(void)
{
    IfxEvadc_Adc_Group *group;
    IfxEvadc_Adc_ChannelConfig adcChannelConfig;

    group = EVADC_Group0_getHandle();

    /* AN1 */
    IfxEvadc_Adc_initChannelConfig(&adcChannelConfig, group);
    adcChannelConfig.channelId = DOOR_CHANNEL_ID;
    adcChannelConfig.resultRegister = DOOR_RESULT_REG;
    IfxEvadc_Adc_initChannel(&g_curDoorChannel, &adcChannelConfig);

    /* AN2 */
    IfxEvadc_Adc_initChannelConfig(&adcChannelConfig, group);
    adcChannelConfig.channelId = WINDOW_CHANNEL_ID;
    adcChannelConfig.resultRegister = WINDOW_RESULT_REG;
    IfxEvadc_Adc_initChannel(&g_curWindowChannel, &adcChannelConfig);

    /* queue */
    IfxEvadc_Adc_addToQueue(&g_curDoorChannel, IfxEvadc_RequestSource_queue0, IFXEVADC_QUEUE_REFILL);
    IfxEvadc_Adc_addToQueue(&g_curWindowChannel, IfxEvadc_RequestSource_queue0, IFXEVADC_QUEUE_REFILL);
}

