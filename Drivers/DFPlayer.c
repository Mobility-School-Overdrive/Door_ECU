/* DFPlayer UART Library */

void uint16ToArray(uint16 value, uint8 *array)
{
    *array = (uint8)(value>>8);
    *(array+1) = (uint8)(value);
}

uint16 calculateCheckSum(uint8 *buffer)
{
    uint16 sum = 0;
    for (int i=Stack_Version; i<Stack_CheckSum; i++) {
        sum += buffer[i];
    }
    return -sum;
}

void sendStack(void)
{
    Ifx_SizeT count = DFPLAYER_SEND_LENGTH;
    IfxAsclin_Asc_write(&g_dfPlayerAsc, _sending, &count, TIME_INFINITE);
}

void dfReset(void)
{
    _sending[Stack_Command]       = CMD_RESET;
    _sending[Stack_Parameter]     = 0x00;
    _sending[Stack_Parameter + 1] = 0x00;

    sendStack();
}

void dfSetVolume(uint8 vol)
{
    _sending[Stack_Command]       = CMD_SET_VOLUME;
    _sending[Stack_Parameter]     = 0x00;
    _sending[Stack_Parameter + 1] = vol;

    sendStack();
}

void playDFPlayerMusic(uint8 folderNumber, uint8 fileNumber)
{
    _sending[Stack_Command] = CMD_PLAY_FOLDER;

    // playFolder(folderNumber, fileNumber)
    _sending[Stack_Parameter]     = folderNumber;  // parameter high byte
    _sending[Stack_Parameter + 1] = fileNumber;    // parameter low byte

    uint16ToArray(calculateCheckSum(_sending), _sending + Stack_CheckSum);
    sendStack();
}

void playDFPlayerMusicLoop(uint8 folderNumber)
{
    _sending[Stack_Command]     = CMD_PLAY_FOLDER_LOOP;
    _sending[Stack_Parameter]   = folderNumber;

    sendStack();
}
