/* UltraSonic Init */

/* This function initializes the TIM to capture a PWM signals */
void init_TIM(void)
{
    IfxGtm_enable(&MODULE_GTM);                                         /* Enable the GTM                           */
    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK0);        /* Enable the CMU clock 0                   */

    IfxGtm_Tim_In_Config configTIM;

    IfxGtm_Tim_In_initConfig(&configTIM, &MODULE_GTM);                  /* Initialize default parameters            */
    configTIM.filter.inputPin = &ULTRASONIC_ECHO_TIM_IN;                /* Select input port pin                    */
    configTIM.filter.inputPinMode = IfxPort_InputMode_noPullDevice;     /* Select input port pin mode               */
    IfxGtm_Tim_In_init(&g_driverTIM, &configTIM);                       /* Initialize the TIM                       */
}

void initUltraSonic(void)
{
    // Echo pin input
    P11_IOCR0.U &= ~(0x1F << PCn_3_IDX);
    P11_IOCR0.U |=  (ULTRASONIC_ECHO_PCx << PCn_3_IDX);

    // Trig pin output
    P11_IOCR4.U &= ~(0x1F << PCn_6_IDX);
    P11_IOCR4.U |=  (ULTRASONIC_TRIG_PCx << PCn_6_IDX);

    // Trig Low
    ULTRASONIC_POUT.U &= ~(1U << TRIG_PIN);

    init_TIM();
}

/*****************/

/* UltraSonic Library */

// read Echo Pulse Width
uint32 getEchoTimeUs(void)
{
    float32 period;
    float32 duty;
    float32 highTimeSec;

    IfxGtm_Tim_In_update(&g_driverTIM);                             // TIM 값 업데이트

    // High 시간 (sec)
    period = IfxGtm_Tim_In_getPeriodSecond(&g_driverTIM);
    duty   = IfxGtm_Tim_In_getDutyPercent(&g_driverTIM, &g_dataCoherent) / 100.0;
    highTimeSec = period * duty;

    return (uint32)(highTimeSec * 1000000);                         // us 변환
}


void Ultrasonic_TriggerStart(void)
{
    ULTRASONIC_POUT.U |= (1U << TRIG_PIN);
}

void Ultrasonic_TriggerStop(void)
{
    ULTRASONIC_POUT.U &= ~(1U << TRIG_PIN);
}

void Ultrasonic_TriggerOneShot(void)
{
    volatile uint32 i;

    if(g_ultraTrigBusy == TRUE)
    {
        return;
    }

    g_ultraTrigBusy = TRUE;

    /* TRIG High */
    ULTRASONIC_POUT.U |= (1U << TRIG_PIN);

    /* 약 10us 정도 delay - 보정 필요 */
    for(i = 0; i < 300; i++) __nop();

    /* TRIG Low */
    ULTRASONIC_POUT.U &= ~(1U << TRIG_PIN);

    g_ultraTrigBusy = FALSE;
}

void Ultrasonic_TriggerOneShotDone(void)
{
    Ultrasonic_TriggerStop();
    g_ultraTrigBusy = FALSE;
}
