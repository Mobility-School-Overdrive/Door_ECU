/* Global */
#define PCn_3_IDX 27
#define PCn_6_IDX 19

#define PCx_NO_PULL         0x0
#define PCx_GENERAL_OUTPUT  0x10

/* UltraSonic */
#define ULTRASONIC_PORT 11
#define TRIG_PIN        6
#define ECHO_PIN        3

#define ULTRASONIC_POUT (P11_OUT)

#define ULTRASONIC_TRIG_PCx (PCx_GENERAL_OUTPUT)
#define ULTRASONIC_ECHO_PCx (PCx_NO_PULL)

#define ULTRASONIC_ECHO_TIM_IN   IfxGtm_TIM2_2_P11_3_IN

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxGtm_Tim_In g_driverTIM;                              /* TIM driver structure                                     */
boolean g_dataCoherent = FALSE;                         /* Boolean to know if the measured data is coherent         */

typedef enum
{
    US_IDLE = 0,
    US_WAIT_ECHO,
    US_DONE
} UltrasonicState;

volatile UltrasonicState g_ultraState = US_IDLE;
volatile uint16 g_distanceCm = 0;
volatile boolean g_ultraReady = FALSE;
volatile boolean g_ultraTrigBusy = FALSE;

/*********************************************************************************************************************/
/*-----------------------------------------------Function Prototypes-------------------------------------------------*/
/*********************************************************************************************************************/

uint32 getEchoTimeUs(void);
void Ultrasonic_TriggerOneShot(void);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* UltraSonic Task */

void Ultrasonic_Task(void)
{
    uint32 durationUs;
    static uint16 triggerIntervalCnt = 0;
    static uint16 echoWaitCnt = 0;

    switch(g_ultraState)
    {
        case US_IDLE:
            triggerIntervalCnt++;

            if(triggerIntervalCnt >= 50U)   // 1ms task 기준 50ms마다 측정 시작
            {
                triggerIntervalCnt = 0U;
                echoWaitCnt = 0U;

                Ultrasonic_TriggerOneShot();
                g_ultraState = US_WAIT_ECHO;
            }
            break;

        case US_WAIT_ECHO:
            echoWaitCnt++;
            durationUs = getEchoTimeUs();

            if(durationUs > 100U && durationUs < 30000U)
            {
                g_distanceCm = (uint16)(durationUs / 58U);
                g_ultraReady = TRUE;
                g_ultraState = US_DONE;
            }
            else if(echoWaitCnt >= 30U)   // 30ms timeout
            {
                g_ultraState = US_IDLE;
            }
            break;

        case US_DONE:
            g_ultraState = US_IDLE;
            break;

        default:
            g_ultraState = US_IDLE;
            break;
    }
}

